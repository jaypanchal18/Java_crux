package session3;

public class else_if {

	public static void main(String[] args) {
		
		int oranges = 10;
		int banana = 13;
		int mango = 15;
		
		if(oranges>10)
		{
			System.out.println("buy oranges");
		}
		else if(banana>12)
		{
			System.out.println("buy banana");
		}
		else if(mango>15)
		{
			System.out.println("buy mango");
		}
		else
		{
			System.out.println("Come back to home");
		}
		
		
	
			

	}

}
